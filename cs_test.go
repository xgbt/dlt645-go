package dlt645

import "testing"

func Test_generateCheckSum(t *testing.T) {
	// 68 01 00 14 57 42 30 68 11 04 33 33 33 33
	raw := []byte{0x68, 0x01, 0x00, 0x14, 0x57, 0x42, 0x30, 0x68, 0x11, 0x04, 0x33, 0x33, 0x33, 0x33} //0x8f, 0x16
	// raw := []byte{0x68, 0x18, 0x35, 0x00, 0x10, 0x02, 0x10, 0x68, 0x11, 0x04, 0x33, 0x34, 0x34, 0x35} //0x24, 0x16
	if got := generateCheckSum(raw); got != 0x8f {
		t.Errorf("generateCheckSum() = %v, want %v", got, 0x8f)
	}
}
